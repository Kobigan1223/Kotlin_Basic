fun main() {
    val name = "Rover"
    val age = 5 * 365
    println("Happy Birthday, ${name}!")
    
    // Let's print a cake!
    println("   ,,,,,   ")
    println("   |||||   ")
    println(" =========")
    println("@@@@@@@@@@@")
    println("{~@~@~@~@~}")
    println("@@@@@@@@@@@")
    
    // This prints an empty line.
    println("")
    println("You are already ${age} days old, ${name}!")
    println("${age} is the very best age to celebrate!")
}